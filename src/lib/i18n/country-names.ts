/**
 * Resolve the country name for a given locale, falling back to the
 * provided English fallback (typically the canonical name from
 * `ALL_COUNTRY_MAP[code].name`).
 */
export function localizedCountryName(
  code: string,
  locale: LocaleCode,
  fallback: string,
): string {
  return COUNTRY_NAMES[code]?.[locale] ?? fallback;
}
